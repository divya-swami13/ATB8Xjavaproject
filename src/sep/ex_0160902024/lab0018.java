package sep.ex_0160902024;

public class lab0018 {
    public static void main(String[] args) {
        // compound Assignment operators
        int a = 10;
        // +=, -=, *=, /=
        a += 20;  // a = a+ 20
        a -= 20; // a = a- 20
        a /= 10; // a = a/10
        a *= 10; // a = a/10
        System.out.println(a);
    }
}
